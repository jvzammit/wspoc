package com.cif.dt.app.chatlog;

@SuppressWarnings("serial")
public class ChatLogHandlerException extends Exception {

	public ChatLogHandlerException() {
	}

	public ChatLogHandlerException(Throwable cause) {
		super(cause);
	}

	public ChatLogHandlerException(String message) {
		super(message);
	}

	public ChatLogHandlerException(String message, Throwable cause) {
		super(message, cause);
	}

}
