package com.cif.dt.domain.dao.chatlog;

@SuppressWarnings("serial")
public class ChatLogDAOException extends Exception {

	public ChatLogDAOException() {
	}

	public ChatLogDAOException(Throwable cause) {
		super(cause);
	}

	public ChatLogDAOException(String message) {
		super(message);
	}

	public ChatLogDAOException(String message, Throwable cause) {
		super(message, cause);
	}

}

